namespace UnikPedel.Infrastructure.Database.ModelConfigurations
{
    public class VicevaertConfiguration : IEntityTypeConfiguration<Domain.Entities.Vicevaert>
    {
        public void Configure(EntityTypeBuilder<Domain.Entities.Vicevaert> entity)
        {
            entity.ToTable("Vicevært");
            entity.HasKey(x => x.Id);
            entity.Property(a => a.Id).HasColumnName("Id").ValueGeneratedOnAdd(); //This tells EF the value to be AutoGenerated on ADD
            entity.Property(a => a.ForNavn).HasColumnName("ForNavn").IsRequired();
            entity.Property(a => a.EfterNavn).HasColumnName("EfterNavn").IsRequired();
            entity.Property(a => a.Email).HasColumnName("Email").IsRequired();
            entity.Ignore(v => v._serviceProvider);

            //entity.HasOne(c => c.EjendomsAnsvarlig).WithMany(d => d.Vicevært).HasForeignKey(x => x.EjendomsAnsvarligId).OnDelete(DeleteBehavior.Cascade);
